void PooGame::Poo::Move(const MainWindow& wnd){
    rect.pos += speed;
    if(rect.pos.x < 0){
        rect.pos.x = 0;
        speed.x = -speed.x;
    }
    if(rect.pos.y < 0){
        rect.pos.y = 0;
        speed.y = -speed.y;
    }
    if(rect.pos.x + rect.size.x > wnd.GetWidth()){
        rect.pos.x = wnd.GetWidth() - rect.size.x;
        speed.x = -speed.x;
    }
    if(rect.pos.y + rect.size.y > wnd.GetHeight()){
        rect.pos.y = wnd.GetHeight() - rect.size.y;
        speed.y = -speed.y;
    }
}

