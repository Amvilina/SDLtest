void PooGame::Dude::Move(const MainWindow& wnd){
    if (wnd.kbd.IsPressed('w'))
        rect.pos.y -=3;
    if (wnd.kbd.IsPressed('s'))
        rect.pos.y+=3;
    if (wnd.kbd.IsPressed('a'))
        rect.pos.x-=3;
    if (wnd.kbd.IsPressed('d'))
        rect.pos.x+=3;
    
}



