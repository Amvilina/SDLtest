#include "PooGame.hpp"

PooGame::PooGame()
:
dude(390, 290, 20, 20),
isStart(false)
{}

void PooGame::UpdateModel(){
    if (isStart) {
        dude.Move(wnd);
        if(!poo1.isEaten)
            poo1.Move(wnd);
        if(!poo2.isEaten)
            poo2.Move(wnd);
        
        if(dude.rect.IsCollide(poo1.rect))
            poo1.isEaten = true;
        if(dude.rect.IsCollide(poo2.rect))
            poo2.isEaten = true;
    }else{
        if(wnd.kbd.IsReleased(' '))
            isStart = true;
    }
    
    
}
void PooGame::ComposeFrame(){
    
    if(isStart){
        dude.Draw(gfx);
        if(!poo1.isEaten)
            poo1.Draw(gfx);
        if(!poo2.isEaten)
            poo2.Draw(gfx);
    }
    
}


